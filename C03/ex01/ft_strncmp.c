int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	res;
	int	c;

	res = 0;
	c = 0;
	while (res == 0 && !(s1[c] == '\0' && s2[c] == '\0') && n > 0)
	{
		res = s1[c] - s2[c];
		c++;
		n--;
	}
	return (res);
}
